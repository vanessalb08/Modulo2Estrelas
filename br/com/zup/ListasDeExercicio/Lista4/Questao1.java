package br.com.zup.ListasDeExercicio.Lista4;

//Faça um programa que mostra na tela os números de 1 a 100

public class Questao1 {
    public static void main(String[] args) {
        int cont = 1;
        while (cont <= 100){
            System.out.println(cont);
            cont++;
        }
    }
}
