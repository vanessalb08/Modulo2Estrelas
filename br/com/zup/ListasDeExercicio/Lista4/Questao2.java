package br.com.zup.ListasDeExercicio.Lista4;

//Faça um programa que mostra na tela os números de 100 a 1

public class Questao2 {
    public static void main(String[] args) {
        int cont = 100;
        while(cont >= 1){
            System.out.println(cont);
            cont--;
        }
    }
}
